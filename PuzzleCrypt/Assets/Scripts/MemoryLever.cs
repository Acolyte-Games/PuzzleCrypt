using UnityEngine;

/// <summary>
/// A lever/switch that starts the memory pressure-pad puzzle.
/// Attach alongside any sprite renderer you want to flip on activation.
/// Implements IInteractable so your existing interaction system works unchanged.
///
/// Flow:
///   Player interacts ? ActivateSwitch() ? PressurePadPuzzle.BeginPuzzle()
///   Puzzle fails     ? PressurePadPuzzle calls ResetLever() ? lever unflips, ready again
///   Puzzle solved    ? lever stays flipped (permanently active), interaction disabled
/// </summary>
public class MemorySwitch : MonoBehaviour, IInteractable
{
    // ?? Public state ?????????????????????????????????????????????????????????
    public bool IsFlipped { get; private set; }

    // ?? Inspector ?????????????????????????????????????????????????????????????
    [Header("Puzzle Reference")]
    [Tooltip("The PressurePadPuzzle manager in this room.")]
    public PressurePadPuzzle puzzle;

    [Header("Visual Feedback")]
    [Tooltip("Sprite shown when the lever is in its default state.")]
    public Sprite spriteDefault;

    [Tooltip("Sprite shown when the lever has been flipped.")]

    // ?? Private ???????????????????????????????????????????????????????????????
    private SpriteRenderer sr;

    // ?????????????????????????????????????????????????????????????????????????
    void Awake()
    {
        sr = GetComponent<SpriteRenderer>();
        RefreshSprite();
    }

    // ?? IInteractable ?????????????????????????????????????????????????????????

    public bool CanInteract()
    {
        // Cannot interact if already flipped, or if the puzzle has been solved.
        if (IsFlipped) return false;
        if (puzzle != null && puzzle.IsSolved) return false;
        return true;
    }

    public void Interact()
    {
        if (!CanInteract()) return;
        ActivateSwitch();
    }

    // ?? Internal logic ????????????????????????????????????????????????????????

    private void ActivateSwitch()
    {
        IsFlipped = true;
        RefreshSprite();
        Debug.Log("[MemorySwitch] Lever flipped — starting puzzle.");

        if (puzzle != null)
            puzzle.BeginPuzzle(this);
        else
            Debug.LogWarning("[MemorySwitch] No PressurePadPuzzle assigned!");
    }

    /// <summary>
    /// Called by PressurePadPuzzle when the player fails, so the lever resets
    /// and can be pulled again.
    /// </summary>
    public void ResetLever()
    {
        IsFlipped = false;
        RefreshSprite();
        Debug.Log("[MemorySwitch] Lever reset — puzzle can be restarted.");
    }

    private void RefreshSprite()
    {
        if (sr == null) return;

        // Always use the same sprite
        if (spriteDefault != null)
            sr.sprite = spriteDefault;

        // Mirror the sprite when flipped
        sr.flipX = IsFlipped;
    }
}